"""Professional Invoice PDF generator — ReportLab.

Layout matches reference Fattura 7/2026:
- Logo top-left, company name/details top-right
- Thin blue divider line
- FATTURA N. XX/XXXX centered and large
- DATA FATTURA | TIPO in two side-by-side boxes below title
- Items table: navy header, white rows, no alternating colors
- Totals block: right-aligned, subtotals plain, total with navy bg
- Coordinate bancarie: left blue bar box
- Scadenza pagamenti: left blue bar box below
- Footer: generated by line + page number
"""
from io import BytesIO
from datetime import datetime
import logging

logger = logging.getLogger(__name__)

from reportlab.lib.pagesizes import A4
from reportlab.lib.units import mm
from reportlab.lib import colors
from reportlab.lib.styles import ParagraphStyle
from reportlab.lib.enums import TA_LEFT, TA_RIGHT, TA_CENTER
from reportlab.platypus import (
    SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle,
    HRFlowable
)
from reportlab.platypus.flowables import PageBreak

COL_NAVY        = colors.HexColor("#1E3A5F")
COL_ACCENT      = colors.HexColor("#0055FF")
COL_DARK        = colors.HexColor("#1a1a2e")
COL_GRAY        = colors.HexColor("#64748b")
COL_LIGHT_GRAY  = colors.HexColor("#F1F5F9")
COL_BORDER      = colors.HexColor("#CBD5E1")
COL_ROW_BORDER  = colors.HexColor("#E2E8F0")
COL_WHITE       = colors.white

DOC_TYPE_NAMES = {
    "FT": "FATTURA",
    "PRV": "PREVENTIVO",
    "DDT": "DOCUMENTO DI TRASPORTO",
    "NC": "NOTA DI CREDITO",
}

PAYMENT_METHOD_NAMES = {
    "bonifico": "Bonifico Bancario a Vista",
    "contanti": "Contanti",
    "carta": "Carta di Credito",
    "assegno": "Assegno",
    "riba": "RiBa",
    "altro": "Altro",
}

PAGE_W = A4[0] - 32*mm


def _fmt(n) -> str:
    try:
        val = float(n or 0)
    except (ValueError, TypeError):
        return "0,00"
    s = f"{val:,.2f}"
    return s.replace(",", "X").replace(".", ",").replace("X", ".")


def _s(val) -> str:
    return str(val or "")


def _date(d) -> str:
    if isinstance(d, datetime):
        return d.strftime("%d/%m/%Y")
    if isinstance(d, str):
        try:
            return datetime.fromisoformat(d.replace("Z", "+00:00")).strftime("%d/%m/%Y")
        except Exception:
            return d
    return ""


def _left_bar_box(content_rows, col_width):
    """Box with left blue accent bar, white background, light border."""
    accent = Table([['']], colWidths=[3*mm], style=TableStyle([
        ('BACKGROUND', (0,0), (-1,-1), COL_ACCENT),
        ('LEFTPADDING', (0,0), (-1,-1), 0),
        ('RIGHTPADDING', (0,0), (-1,-1), 0),
        ('TOPPADDING', (0,0), (-1,-1), 0),
        ('BOTTOMPADDING', (0,0), (-1,-1), 0),
    ]))
    inner = Table(content_rows, colWidths=[col_width - 5*mm], style=TableStyle([
        ('BACKGROUND', (0,0), (-1,-1), COL_WHITE),
        ('LEFTPADDING', (0,0), (-1,-1), 8),
        ('RIGHTPADDING', (0,0), (-1,-1), 8),
        ('TOPPADDING', (0,0), (-1,-1), 4),
        ('BOTTOMPADDING', (0,0), (-1,-1), 4),
    ]))
    outer = Table([[accent, inner]], colWidths=[3*mm, col_width - 3*mm])
    outer.setStyle(TableStyle([
        ('VALIGN', (0,0), (-1,-1), 'TOP'),
        ('LEFTPADDING', (0,0), (-1,-1), 0),
        ('RIGHTPADDING', (0,0), (-1,-1), 0),
        ('TOPPADDING', (0,0), (-1,-1), 0),
        ('BOTTOMPADDING', (0,0), (-1,-1), 0),
        ('BOX', (0,0), (-1,-1), 0.5, COL_BORDER),
    ]))
    return outer


